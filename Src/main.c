/**
  * @brief  Period elapsed callback in non blocking mode
  * @note   This function is called  when TIM6 interrupt took place, inside
  * HAL_TIM_IRQHandler(). It makes a direct call to HAL_IncTick() to increment
  * a global variable "uwTick" used as application time base.
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* USER CODE BEGIN Callback 0 */
	if (htim->Instance == TIM14) 
	{
		if (flag_delay900ms)
			tim14_delay900ms ++;
		if (tim14_delay900ms >= 800) 
		{
			tim14_delay900ms = 0;
			if (flag_lock == 1)
			{
				flag_delay_lock = 1;
			}
			else if (flag_lock == 2)
			{
				flag_delay_unlock = 1;
			}
		}
		
		if (flag_delay500ms)
			tim14_delay500ms ++;
		if (tim14_delay500ms >= 500) 
		{
			tim14_delay500ms = 0;
			flag_batlock = 1;
		}
		if (flag_delay10ms)
			tim14_delay10ms ++;
		if (tim14_delay10ms >= 1)
		{
			tim14_delay10ms = 0;
			flag_motorlock = 1;
		}
		if (flag_delay4s)
			tim14_delay4s++;
		if (tim14_delay4s >= 4000) 
		{
			tim14_delay4s = 0;
			flag_motorlock2 = 1;
		}

		if (flag_delay8s)
			tim14_delay8s++;
		if (tim14_delay8s >= 8000) 
		{
			tim14_delay8s = 0;
			flag_alarm = 0;
			flag_delay8s = 0;
		}
		
	}
	
	/* USER CODE END Callback 0 */
	if (htim->Instance == TIM6)
	{
	HAL_IncTick();
	}
  	/* USER CODE BEGIN Callback 1 */
	if (htim->Instance == htim3.Instance)
	{
		tim3_delay3s++;

		if (tim3_delay3s >= 3) 
		{
			tim3_delay3s = 0;
			flag_delay3s = 1;
		}
		flag_delay1s = 1;
		
		diff_rotate = rotate_count - rotate_bak;
		diff_mileage = mileage_count - mileage_bak;
		diff_shake = shake_count - shake_bak;
		
		rotate_bak = rotate_count;
		mileage_bak = mileage_count;
		shake_bak = shake_count;

//		Logln(D_INFO,"Feed WatchDog");
		HAL_IWDG_Refresh(&hiwdg);//5s内必须喂看门狗，不然系统会复位

	}
  /* USER CODE END Callback 1 */
}

