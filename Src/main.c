/**
  * @brief  Rx Transfer completed callback
  * @param  UartHandle: UART handle
  * @note   This example shows a simple way to report end of DMA Rx transfer, and 
  *         you can add your own implementation.
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *UartHandle)
{
  /* Set transmission flag: transfer complete */
	if (UartHandle->Instance == USART1) 
	{
		module_recv_buffer[module_recv_write_index] = usart1_recbuf; //将接收到的字符串存到缓存中
		module_recv_write_index++;
		if(module_recv_write_index >= MODULE_BUFFER_SIZE)       			//如果缓存满,将缓存指针指向缓存的首地址
		{
			module_recv_write_index = 0;
		}
		 HAL_UART_Receive_IT(&huart1, (uint8_t *)&usart1_recbuf, 1);
       	}
	if (UartHandle->Instance == USART2) 
	{
		Uart2_buf.Data[0]= usart2_recbuf;
		PushElement(&RxUart2_Queue,Uart2_buf,1);//将接收到的数据压入队列中
		HAL_UART_Receive_IT(&huart2, &usart2_recbuf, 1);
	}
	if (UartHandle->Instance == USART3) 
	{
		Uart3_buf.Data[0]= usart3_recbuf;
		PushElement(&RxUart3_Queue,Uart3_buf,1);//将接收到的数据压入队列中
		HAL_UART_Receive_IT(&huart3, &usart3_recbuf, 1);
	}
}
